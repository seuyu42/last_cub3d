#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	size_t			i;
	unsigned char	*str;

	str = (unsigned char*)b;
	i = 0;
	while (i < len)
	{
		str[i] = 0;
		i++;
	}
	return (b);
}
